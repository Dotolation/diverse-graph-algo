import pandas as pd
import sys

def analysis(df_whole, algo_type, k):
  print("\n --------{}--------".format(algo_type))
  df = df_whole.query("Algo_Type==\"{}\" and k=={}".format(algo_type, k))
  print(df.groupby("Instance_Name").mean())

def compare_diverse_kbest(csv_filename, ks=["10"]):
  df = pd.read_csv(csv_filename)[["Instance_Name","Algo_Type", "k", "Milliseconds", "Diversity"]]
  for k in ks:
    analysis(df, "Diverse", int(k))
    analysis(df, "KBest", int(k))

if (len(sys.argv)>2):
  compare_diverse_kbest(sys.argv[1], sys.argv[2:])
else:
  compare_diverse_kbest(sys.argv[1])